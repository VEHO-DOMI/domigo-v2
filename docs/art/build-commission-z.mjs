// Build CODEX_COMMISSION_Z.html + CODEX_MASTER_PROMPT_Z.md from commission-z.mjs
// (THE PAINTED BOOK — the M1 style-key gate, doc 31 §8). Batch Z is ALL
// gate-only: five images, judged by eye, never sliced — the machine check here
// asserts exactly that (a non-gate card in Z without a manifest = drift).
//   node docs/art/build-commission-z.mjs [--refs <dir>]
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { cardsZ, futureSectionsZ, STYLE_PAINT_V1, PALETTE_U01, HERO_PAINT } from "./commission-z.mjs";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const refsDir = process.argv.includes("--refs") ? process.argv[process.argv.indexOf("--refs") + 1] : path.join(HERE, "refs-z");

const nonGate = cardsZ.filter((c) => c.gateOnly !== true);
if (nonGate.length > 0) {
  console.error("BATCH Z DRIFT: non-gate cards present but Z has no slicing manifest:", nonGate.map((c) => c.stem));
  process.exit(1);
}
console.log(`gate check OK: ${cardsZ.length} cards, all gate-only (eyeball, never sliced)`);

// ── references (base64) — OUR OWN painted art only (CP-15) ──
const refs = [];
try {
  for (const f of fs.readdirSync(refsDir).filter((x) => /\.(png|jpg)$/i.test(x)).sort()) {
    const b64 = fs.readFileSync(path.join(refsDir, f)).toString("base64");
    refs.push({ name: f, src: `data:image/${f.endsWith(".jpg") ? "jpeg" : "png"};base64,${b64}` });
  }
} catch { console.warn("no refs dir — building without embedded references"); }

const esc = (s) => s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
const fmtLabel = (f) => (f === "sheet" ? "MAGENTA-SHEET (harte Silhouetten — CP-9)" : "FULL-BLEED (gemalt)");

const cardHtml = (c, i) => `
<div class="card gate">
  <div class="head"><span class="n">${i + 1}</span><code>${c.file}</code><span class="cls">🎨 GEMALT</span><span class="gatechip">🚦 TOR-BILD</span><span class="meta">${c.size} · ${fmtLabel(c.format)}</span></div>
  <div class="usage">${esc(c.usage)}</div>
  <div class="drop">Speichern als: <code>~/Code/codex-art-lab/batch-z/${c.folder === "root" ? "" : c.folder + "/"}${c.file}</code></div>
  <details open><summary>Prompt (komplett kopieren → Codex)</summary><pre>${esc(c.prompt)}</pre></details>
</div>`;

