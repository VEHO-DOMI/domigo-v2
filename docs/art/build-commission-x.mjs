// Build CODEX_MASTER_PROMPT_X.md (+ a light HTML) from commission-x.mjs.
// Small surgical batch — same sandbox law, gate-first on card 1 (the climb).
//   node docs/art/build-commission-x.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { cardsX, futureSectionsX, STYLE_PIXEL_V3 } from "./commission-x.mjs";

const HERE = path.dirname(fileURLToPath(import.meta.url));

export const SLICES_X = {
  climb_nopole_sheet: ["hero2_climb1", "hero2_climb2"],
  acc3_sheet: ["acc_cap", "acc_scarf"],
  maphero3_sheet: ["mh_down1", "mh_down2", "mh_up1", "mh_up2", "mh_side1", "mh_side2"],
  alphabet3_sheet: ["alpha_a", "alpha_b", "alpha_c", "alpha_d", "alpha_e", "alpha_f", "alpha_g", "alpha_h", "alpha_i", "alpha_j", "alpha_k", "alpha_l", "alpha_m", "alpha_n", "alpha_o", "alpha_p", "alpha_q", "alpha_r", "alpha_s", "alpha_t", "alpha_u", "alpha_v", "alpha_w", "alpha_x", "alpha_y", "alpha_z"],
  hazard3_strip: ["prop_spikes"],
  levelprops3_sheet: ["prop_flag", "prop_door_open"],
};

const yielded = [];
for (const c of cardsX) for (const s of SLICES_X[c.stem] ?? [c.stem]) yielded.push(s);
console.log(`Batch X: ${cardsX.length} cards → ${yielded.length} stems (all onto legacy engine slots)`);

const stripStyle = (p) => p.replace(STYLE_PIXEL_V3, "").replace(/^\s*\n/, "");
const master = `# MISSION: produce the image set for "DomiGo — Batch X" (v5.1 surgical fixes, ${cardsX.length} images)

You are a senior HD pixel artist executing a fixed commission for a children's
English-learning game. Work AUTONOMOUSLY through the cards IN ORDER.

## ★ THE GATE: generate ONLY CARD 1 (the pole-free climb pair) first, then STOP
and print exactly: \`GATE CARD DONE — awaiting look approval before cards 2+\`.
Card 1 replaces frames that live INSIDE the game's climbing system — it must
match Batch W's hero exactly before the rest runs.

## THE STYLE CONTRACT (every card)
${STYLE_PIXEL_V3}

## WORKING RULES
0b. READ THE METHOD FIRST: \`~/Code/codex-art-lab/CODEX_METHOD.md\` (sandbox law,
   CP-registry). If missing, say so and continue.
0a. STUDY \`~/Code/codex-art-lab/batch-w/hero/hero3_sheet.png\` — the canonical
   hero these cards must match EXACTLY (costume, proportions, rendering).
0. YOUR ONE AND ONLY WRITE LOCATION: \`~/Code/codex-art-lab/batch-x/\` (create it).
   NEVER write anywhere else.
1. Before each image print \`NOW GENERATING: <filename>\`; save to the card's
   SAVE TO path; print \`SAVED: <full path>\`.
2. AFTER each image, self-check (PASS/FAIL per line; one retry on FAIL):
   - MATCH: the hero/props sit beside batch-w art as the same craft tier
   - SMOOTHNESS: clean tonal bands, no speckle noise
   - FORMAT: solid #FF00FF magenta sheet, clean grid, nothing touching borders
   - SUBJECT: every element present; card 1 contains NO pole/pencil anywhere
3. If you hit a limit, print \`CONTINUE AT CARD <n>\` and resume when restarted.
4. Never invent extra images, text, watermarks, or borders.

## THE CARDS
${cardsX.map((c, i) => `---
CARD ${i + 1} · filename: ${c.file}${i === 0 ? " · 🚦 GATE" : ""}
SAVE TO: ~/Code/codex-art-lab/batch-x/${c.folder}/${c.file}
SIZE: ${c.size}
FORMAT: pose-sheet on SOLID magenta #FF00FF
USED FOR: ${c.usage}
SUBJECT BRIEF:
${stripStyle(c.prompt)}`).join("\n\n")}

---
END OF COMMISSION. After the final card, print a manifest of every filename,
in order, marked DONE or REGENERATED.
`;
fs.writeFileSync(path.join(HERE, "CODEX_MASTER_PROMPT_X.md"), master);
console.log(`CODEX_MASTER_PROMPT_X.md written (${(master.length / 1024).toFixed(0)} KB)`);

const esc = (s) => s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
const html = `<!-- GENERATED by build-commission-x.mjs -->
<meta charset="utf-8"><title>DomiGo · Codex-Kommission — Batch X (v5.1 Chirurgie)</title>
<style>body{font-family:-apple-system,system-ui,sans-serif;max-width:940px;margin:24px auto;padding:0 18px;background:#faf8f2;color:#1c1a28;line-height:1.5}
.card{background:#fff;border:1px solid #ddd6c4;border-left:5px solid #2a9d5c;border-radius:12px;padding:14px 16px;margin:14px 0}
.card.gate{background:#fffaf0;border:2px solid #e0a92a}pre{white-space:pre-wrap;background:#f4f1e6;border-radius:8px;padding:10px;font-size:12.5px}</style>
<h1>🖋 Batch X — v5.1 Chirurgie (${cardsX.length} Bilder)</h1>
<p><b>Für Koki.</b> Kleiner Präzisions-Batch: stangefreies Klettern (🚦 TOR zuerst), Accessoires v3, HD-Kartenheld, HD-Alphabet, lesbarer Gefahren-Streifen, geerdete Fahne+Tür. Vorgehen wie immer: <code>CODEX_MASTER_PROMPT_X.md</code> in Codex einfügen — Codex rendert Karte 1 und HÄLT AN für deinen Blick.</p>
${cardsX.map((c, i) => `<div class="card${i === 0 ? " gate" : ""}"><b>${i + 1} · ${c.file}</b> ${i === 0 ? "🚦 TOR" : ""} — ${c.size}<div>${esc(c.usage)}</div><details open><summary>Prompt</summary><pre>${esc(c.prompt)}</pre></details></div>`).join("")}
${futureSectionsX.map((s) => `<div class="card"><b>${s.title}</b><div>${esc(s.note)}</div></div>`).join("")}
`;
fs.writeFileSync(path.join(HERE, "CODEX_COMMISSION_X.html"), html);
console.log("CODEX_COMMISSION_X.html written");
